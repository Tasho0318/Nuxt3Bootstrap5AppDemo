ドイツ人初のF1ドライバーズチャンピオン。最多優勝91回、チャンピオン獲得7度などF1の主な個人記録を更新した。2006年に一度引退したが2010年に復帰し、2012年に再度現役を退いた。

その正確なドライビングと、強靭なフィジカルから、日本では「ターミネーター」、日本国外では「サイボーグ」と呼ばれる時期があった。フェラーリ在籍期、フジテレビのF1中継では「皇帝」の愛称が使われた。

