シューマッハには外傷性脳損傷が認められたため、彼は人工的な昏睡状態に置かれ、2014年3月7日には担当医によって容体が安定していることが報告された。2014年6月16日までに、シューマッハは意識を回復し、リハビリのためスイスのローザンヌ大学病院（英語版）に転院した。2014年9月9日、シューマッハはローザンヌ大学病院を退院し、さらなるリハビリを行うため自宅に戻された。 2014年11月、元F1ドライバーのフィリップ・ストレイフによる情報として、シューマッハが「麻痺のため車椅子を使用している」こと、また「喋ることができず記憶障害がある」ことが報道された。2015年5月に公開されたインタビュー動画の中で、シューマッハのマネージャーであるサビーネ・ケームは、彼の容体が「負傷の深刻さを考慮すれば」徐々に改善してきていると述べた。

