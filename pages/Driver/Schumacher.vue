2019年7月、フェラーリの元チーム代表ジャン・トッドはラジオ・モンテカルロ（英語版）によるインタビューに応え、スイスにあるシューマッハの自宅で一緒にF1レースをテレビ観戦したと語り、シューマッハは「順調な経過を見せている」ものの「意思疎通は難しい」と述べた。
